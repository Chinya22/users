function createUser(user) {
  const cardsWrapper = document.querySelector(".cards_wrapper");
  const card = document.createElement("div");
  card.classList.add("cards_wrapper_item");

  const img = document.createElement("img");
  img.src = "img/" + user.id + ".png";
  img.classList.add("cards_wrapper_item_img");
  card.append(img);

  const infoWrapper = document.createElement("div");
  infoWrapper.classList.add("cards_wrapper_item_info");
  card.append(infoWrapper);

  const name = document.createElement("div");
  name.classList.add("cards_wrapper_item_info_name");
  name.innerText = user.name;
  const nameSpan = document.createElement("span");
  nameSpan.innerText = "Name: ";
  name.prepend(nameSpan);
  infoWrapper.append(name);

  const username = document.createElement("div");
  username.classList.add("cards_wrapper_item_info_username");
  username.innerText = user.username;
  const usernameSpan = document.createElement("span");
  usernameSpan.innerText = "Username: ";
  username.prepend(usernameSpan);
  infoWrapper.append(username);

  const email = document.createElement("div");
  email.classList.add("cards_wrapper_item_info_email");
  email.innerText = user.email;
  const emailSpan = document.createElement("span");
  emailSpan.innerText = "Email: ";
  email.prepend(emailSpan);
  infoWrapper.append(email);

  const adressWrapper = document.createElement("div");
  adressWrapper.classList.add("cards_wrapper_item_info_adress");
  infoWrapper.append(adressWrapper);

  const street = document.createElement("div");
  street.classList.add("cards_wrapper_item_info_adress_street");
  street.innerText = user.address.street;
  const streetSpan = document.createElement("span");
  streetSpan.innerText = "Street: ";
  street.prepend(streetSpan);
  adressWrapper.append(street);

  const suite = document.createElement("div");
  suite.classList.add("cards_wrapper_item_info_adress_suite");
  suite.innerText = user.address.suite;
  const suiteSpan = document.createElement("span");
  suiteSpan.innerText = "Suite: ";
  suite.prepend(suiteSpan);
  adressWrapper.append(suite);

  const city = document.createElement("div");
  city.classList.add("cards_wrapper_item_info_adress_city");
  city.innerText = user.address.city;
  const citySpan = document.createElement("span");
  citySpan.innerText = "City: ";
  city.prepend(citySpan);
  adressWrapper.append(city);

  const zip = document.createElement("div");
  zip.classList.add("cards_wrapper_item_info_adress_zip");
  zip.innerText = user.address.zipcode;
  const zipSpan = document.createElement("span");
  zipSpan.innerText = "Zip code: ";
  zip.prepend(zipSpan);
  adressWrapper.append(zip);

  const phone = document.createElement("div");
  phone.classList.add("cards_wrapper_item_info_phone");
  phone.innerText = user.phone;
  const phoneSpan = document.createElement("span");
  phoneSpan.innerText = "Phone: ";
  phone.prepend(phoneSpan);
  infoWrapper.append(phone);

  const site = document.createElement("div");
  site.classList.add("cards_wrapper_item_info_site");
  site.innerText = user.website;
  const siteSpan = document.createElement("span");
  siteSpan.innerText = "Site: ";
  site.prepend(siteSpan);
  infoWrapper.append(site);

  const characteristicsWrapper = document.createElement("div");
  characteristicsWrapper.classList.add(
    "cards_wrapper_item_info_characteristics"
  );
  infoWrapper.append(characteristicsWrapper);

  const company = document.createElement("div");
  company.classList.add("cards_wrapper_item_info_characteristics_company");
  company.innerText = user.company.name;
  const companySpan = document.createElement("span");
  companySpan.innerText = "Company: ";
  company.prepend(companySpan);
  characteristicsWrapper.append(company);

  const prase = document.createElement("div");
  prase.classList.add("cards_wrapper_item_info_characteristics_catch");
  prase.innerText = user.company.catchPhrase;
  const praseSpan = document.createElement("span");
  praseSpan.innerText = "Catch prase: ";
  prase.prepend(praseSpan);
  characteristicsWrapper.append(prase);

  const bs = document.createElement("div");
  bs.classList.add("cards_wrapper_item_info_characteristics_bs");
  bs.innerText = user.company.bs;
  const bsSpan = document.createElement("span");
  bsSpan.innerText = "BS: ";
  bs.prepend(bsSpan);
  characteristicsWrapper.append(bs);

  const buttonsWrapper = document.createElement("div");
  buttonsWrapper.classList.add("cards_wrapper_item_info_buttons");
  infoWrapper.append(buttonsWrapper);

  const buttonEdit = document.createElement("button");
  buttonEdit.classList.add(
    "cards_wrapper_item_info_buttons_item",
    "modal_open"
  );

  buttonEdit.innerText = "Edit";
  buttonsWrapper.append(buttonEdit);

  const buttonDelete = document.createElement("button");
  buttonDelete.classList.add("cards_wrapper_item_info_buttons_item");
  buttonDelete.innerText = "Delete";
  buttonsWrapper.append(buttonDelete);

  cardsWrapper.append(card);
}

